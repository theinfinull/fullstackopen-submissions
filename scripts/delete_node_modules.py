#!/usr/bin/env python3

from pathlib import Path
import shutil
import sys

RED = "\033[31m"
GREEN = "\033[32m"
YELLOW = "\033[33m"
BOLD = "\033[1m"
RESET = "\033[0m"

root = Path(".")
node_modules = sorted(
    p for p in root.rglob("node_modules") if p.is_dir()
)

if not node_modules:
    print(f"{GREEN}no node_modules folders found.{RESET}")
    sys.exit(0)

print(f"{BOLD}found {len(node_modules)} node_modules folders:{RESET}\n")

for p in node_modules:
    print(f"  {YELLOW}{p}{RESET}")

print()
answer = input(
    f"{RED}delete all of them? [y/N]: {RESET}"
).strip().lower()

if answer != "y":
    print("aborted.")
    sys.exit(0)

for p in node_modules:
    print(f"deleting {YELLOW}{p}{RESET}")
    shutil.rmtree(p)

print(f"\n{GREEN}✓ all node_modules folders removed.{RESET}")
