#!/usr/bin/env python3

from pathlib import Path

# Colors
RESET = "\033[0m"
BOLD = "\033[1m"
CYAN = "\033[36m"
GREEN = "\033[32m"
YELLOW = "\033[33m"
DIM = "\033[2m"

def dir_size(path: Path) -> int:
    total = 0
    try:
        for f in path.rglob("*"):
            if f.is_file():
                total += f.stat().st_size
    except (PermissionError, FileNotFoundError):
        pass
    return total


def human(size: int) -> str:
    s = float(size)
    units = ("B", "KB", "MB", "GB", "TB")
    for unit in units:
        if s < 1024 or unit == units[-1]:
            return f"{s:.2f} {unit}"
        s /= 1024
    raise AssertionError("unreachable")


root = Path(".")

print(f"{BOLD}scanning for node_modules...{RESET}")

node_modules = [p for p in root.rglob("node_modules") if p.is_dir()]

module_sizes = []
combined = 0

for nm in node_modules:
    size = dir_size(nm)
    combined += size
    module_sizes.append((size, nm))

total = dir_size(root)
without = total - combined

print(f"\n{BOLD}💾 project size report{RESET}\n")
print(f"{CYAN}current total size{RESET}        : {BOLD}{human(total)}{RESET}")
print(f"{YELLOW}all node_modules combined{RESET} : {BOLD}{human(combined)}{RESET}")
print(f"{GREEN}without node_modules{RESET}      : {BOLD}{human(without)}{RESET}")

print(f"\n{BOLD}individual node_modules sizes:{RESET}")
for size, path in sorted(module_sizes, reverse=True):
    print(f"  {CYAN}{human(size):>10}{RESET}  {DIM}{path}{RESET}")
